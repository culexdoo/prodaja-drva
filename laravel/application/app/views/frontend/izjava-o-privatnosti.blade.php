            računalu. Zadane aktivnosti spremanja i slanja cookiea vama nisu vidljive. Ipak, možete promijeniti vaše postavke internet preglednika, odnosno sami odabrati hoćete li zahtjeve za spremanje cookiea odobriti ili odbiti, ili pak pobrisati spremljene cookie automatski pri zatvaranju internet preglednika.<br><br>

            Cookies se mogu odbiti ili izbrisati iz odgovarajućih postavki preglednika. U ovom slučaju
            moguće je da se sve funkcije web stranice koriste u cjelini. Isto tako, neki tehnički podaci,
            uključujući IP adresu kupca, biti će prikupljeni i pohranjeni od strane softvera koji se koristi.
